/** Tipos de practicas medicas que se realizan en la veterinaria. */
public enum PracticaMedica
{
    CONSULTA_GENERAL,       //Revisión completa del animal para evaluar su estado de salud genera
    VACUNACION,             //Aplicación de vacunas para prevenir enfermedades específicas según la especie y edad del animal
    DESPARASITACION,        //Tratamiento para eliminar parásitos internos o externos
    URGENCIA,               //Atención inmediata en caso de accidentes, enfermedades graves, o síntomas agudos
    CIRUGIA,                //Procedimientos quirúrgicos, desde esterilización hasta cirugías más complejas
    CONTROL,                //Asesoría y chequeos para el manejo del peso y la dieta
    ODONTOLOGIA             //Limpieza y tratamiento de los dientes y encías
}
